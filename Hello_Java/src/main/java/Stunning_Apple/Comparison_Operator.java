package Stunning_Apple;

public class Comparison_Operator {
    public static void main(String[] args) {
        int ashu = 90;
        int cash = 78;
        System.out.println(ashu>cash);
        System.out.println(ashu>=cash);
        System.out.println(ashu<cash);
        System.out.println(ashu<=cash);
        System.out.println(ashu==cash);
        System.out.println(ashu!=cash);

    }
    
}
